    private void registroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registroActionPerformed
        String nombre = ingreso.getText();
        String precio = ingresoPrecio.getText();
        double numero = 0;
        try {
            numero = Double.parseDouble(precio);
            if (numero > 0) {
                if (nombre.trim().length() > 0) {
                    ConjuntoProductos.addProducto(new Producto(nombre, (float) numero));
                    salida.setText("Se ha registrado el nombre correctamente.");
                    ingreso.setText("");
                    ingresoPrecio.setText("");
                } else {
                    salida.setText("No se ha ingresado un nombre v√°lido.");
                }

            } else {
                salida.setText("El precio no es v√°lido.");

            }

        } catch (NumberFormatException e) {
            salida.setText("El precio no es v√°lido.");
        }


    }//GEN-LAST:event_registroActionPerformed

