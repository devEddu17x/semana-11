    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        String nombre_buscar = ingresoBuscar.getText();
        if (nombre_buscar.trim().length() > 0) {
            Producto producto = ConjuntoProductos.buscarNombre(nombre_buscar);
            if (producto != null){
                salidaBuscar.setText(producto.toString());
            }
            else {
                salidaBuscar.setText("El producto no se encuentra en la lista");
            }

        } else {
            salidaBuscar.setText("No se ha ingresado un nombre v√°lido.");
        }

    }//GEN-LAST:event_BuscarActionPerformed

