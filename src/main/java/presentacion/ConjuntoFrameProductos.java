    private void seleccionPrecioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seleccionPrecioActionPerformed
        int index = listaOrdenar2.getSelectedIndex();
        
        if (index == 0) {
            ConjuntoProductos.ordenarAsPrecio();
            listaContenidoOrdenar2.setText(ConjuntoProductos.getContenido());
        } else {
            ConjuntoProductos.ordenarDesPrecio();
            listaContenidoOrdenar2.setText(ConjuntoProductos.getContenido());
        }
    }//GEN-LAST:event_seleccionPrecioActionPerformed

